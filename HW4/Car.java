class Car {
    String brand;
    String model;
    int yearOfIssue;
    String color;
    double price;
    String registrationNumber;

    Car(String brand, String model, int yearOfIssue, String color, double price, String registrationNumber) {
        this.brand = brand;
        this.model = model;
        this.yearOfIssue = yearOfIssue;
        this.color = color;
        this.price = price;
        this.registrationNumber = registrationNumber;
    }
}
